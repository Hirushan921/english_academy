    public void loadClass() {
        try {
            ResultSet rs = MySQL.search("SELECT * FROM `class`\n"
                    + "INNER JOIN `status` ON `class`.`status_id`=`status`.`id`\n"
                    + "WHERE `status`.`name`='Active' \n"
                    + "ORDER BY `class`.`id` ASC");
            DefaultTableModel dtm = (DefaultTableModel) jTable4.getModel();
            dtm.setRowCount(0);

            while (rs.next()) {
                Vector v = new Vector();
                v.add(rs.getString("class.id"));
                v.add(rs.getString("class.name"));
                v.add(rs.getString("class.timeslot"));
                v.add(rs.getString("class.year"));

                dtm.addRow(v);
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
    }

    public void loadDuePayment() {
        try {
            ResultSet rs = MySQL.search("SELECT * FROM `invoice`\n"
                    + "INNER JOIN `invoice_item` ON `invoice`.`id`=`invoice_item`.`invoice_id`\n"
                    + "INNER JOIN `invoice_payment` ON `invoice`.`id`=`invoice_payment`.`invoice_id`\n"
                    + "INNER JOIN `class` ON `invoice_item`.`class_id`=`class`.`id`\n"
                    + "INNER JOIN `month` ON `month`.`id`=`invoice_item`.`month_id`\n"
                    + "WHERE `invoice_payment`.`balance`<'0'");

            DefaultTableModel dtm = (DefaultTableModel) jTable6.getModel();
            dtm.setRowCount(0);

            while (rs.next()) {
                Vector v = new Vector();
                v.add(rs.getString("invoice.id"));
                v.add(rs.getString("invoice.student_sin"));
                v.add(rs.getString("class.name"));
                v.add(rs.getString("month.name") + "" + rs.getString("invoice_item.year"));
                v.add(rs.getString("invoice.date_time"));
                v.add(rs.getString("invoice.user_id"));
                v.add(rs.getString("invoice_payment.payment") + "0");
                String balance = rs.getString("invoice_payment.balance");
                String nbalance = balance.substring(1);
                v.add(nbalance + "0");

                dtm.addRow(v);
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
    }

