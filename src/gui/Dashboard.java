    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        if (jTable6.getRowCount() == 0) {
            JOptionPane.showMessageDialog(this, "No Due Payments for Print..", "Warning", JOptionPane.WARNING_MESSAGE);
        } else {
            try {
                SimpleDateFormat sdf2 = new SimpleDateFormat("yyyy-MM-dd HH:mm:ss");
                String dNow = sdf2.format(new Date());

                String issued = String.valueOf(Login.userId);

                InputStream filePath = getClass().getResourceAsStream("/reports/duepay_report.jrxml");
                JasperReport jr = JasperCompileManager.compileReport(filePath);

                HashMap parameters = new HashMap();
                parameters.put("nowdate", dNow);
                parameters.put("issued", issued);

                TableModel tm = jTable6.getModel();
                JRTableModelDataSource dataSource = new JRTableModelDataSource(tm);

                JasperPrint jp = JasperFillManager.fillReport(jr, parameters, dataSource);
                JasperViewer.viewReport(jp, false);
            } catch (Exception e) {
                e.printStackTrace();
            }
        }
    }//GEN-LAST:event_jButton3ActionPerformed

