    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed

        if (jTable1.getRowCount() == 0) {
            JOptionPane.showMessageDialog(this, "Please add details", "Warning", JOptionPane.WARNING_MESSAGE);
        } else {

            // exam_insert
            long mTime = System.currentTimeMillis();
            String uniqueId = mTime + "-" + Login.userId;
            String classId = jLabel7.getText();
            String classDate = jLabel14.getText();
            String examName = jLabel13.getText();

            MySQL.iud("INSERT INTO `exam`(`exam_name`,`date`,`class_id`,`user_id`,`unique_id`) VALUES('" + examName + "','" + classDate + "','" + classId + "','" + Login.userId + "','" + uniqueId + "')");
            // exam_insert

            try {

                ResultSet rs = MySQL.search("SELECT * FROM `exam` WHERE `unique_id`='" + uniqueId + "'");
                rs.next();
                String id = rs.getString("id");

                //marks_insert
                for (int i = 0; i < jTable1.getRowCount(); i++) {
                    String st_sin = jTable1.getValueAt(i, 0).toString();
                    String ex_mark = jTable1.getValueAt(i, 2).toString();

                    MySQL.iud("INSERT INTO `exam_marks`(`exam_id`,`student_sin`,`mark`) VALUES('" + id + "','" + st_sin + "','" + ex_mark + "')");

                }
                //marks_insert

                int option = JOptionPane.showConfirmDialog(this, "New Exam Marks Saved.. Do you want to print report?", "Confirmation", JOptionPane.YES_NO_OPTION, JOptionPane.INFORMATION_MESSAGE);
                if (option == JOptionPane.YES_OPTION) {
                    //print report
                    printExReport();
                }

                resetFields();

            } catch (Exception e) {
                e.printStackTrace();
            }

        }
    }//GEN-LAST:event_jButton4ActionPerformed

