    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed

        String classdata = jComboBox2.getSelectedItem().toString();
        String examName = jTextField1.getText();
        Date date = jDateChooser1.getDate();

        //        System.out.println(action);
        //        System.out.println(date);
        //        System.out.println(classname);
        if (classdata.equals("Select")) {
            JOptionPane.showMessageDialog(this, "Please select a class", "Warning", JOptionPane.WARNING_MESSAGE);
        } else if (examName.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please enter exam name", "Warning", JOptionPane.WARNING_MESSAGE);
        } else if (date == null) {
            JOptionPane.showMessageDialog(this, "Please enter date", "Warning", JOptionPane.WARNING_MESSAGE);
        } else {

            String splitData[] = classdata.split("/", 3);
            String classId = splitData[0];
            String className = splitData[1];
            String classTimeslot = splitData[2];

            String edate = sdf.format(date);

            try {
                ResultSet rs_ex = MySQL.search("SELECT * FROM `exam` WHERE `exam_name`='" + examName + "' AND `date`='" + edate + "' AND `class_id`='" + classId + "'");
                if (rs_ex.next()) {
                    JOptionPane.showMessageDialog(this, "This exam already added..", "Warning", JOptionPane.WARNING_MESSAGE);
                    DefaultTableModel dtm = (DefaultTableModel) jTable1.getModel();
                    dtm.setRowCount(0);
                } else {
                    jLabel7.setText(classId);
                    jLabel9.setText(className);
                    jLabel13.setText(examName);
                    jLabel14.setText(edate);
                    jLabel7.setForeground(Color.BLUE);
                    jLabel9.setForeground(Color.BLUE);
                    jLabel13.setForeground(Color.BLUE);
                    jLabel14.setForeground(Color.BLUE);
                    jComboBox1.setEnabled(false);
                    jComboBox1.setSelectedIndex(0);
                    jDateChooser1.setEnabled(false);
                    jTextField1.setEnabled(false);
                    jComboBox2.setEnabled(false);
                    jButton2.setEnabled(false);
                    jButton4.setEnabled(true);
                    jButton3.setEnabled(false);
                    jButton5.setEnabled(false);
                    loadStudentHasClass();
                }
                jTextField1.setText("");
                jDateChooser1.setDate(null);
                jComboBox2.setSelectedIndex(0);
                jComboBox3.setEnabled(false);
            } catch (Exception e) {
                e.printStackTrace();
            }

        }
    }//GEN-LAST:event_jButton2ActionPerformed

    public void loadStudentHasClass() {
        String class_id = jLabel7.getText();
        try {
            ResultSet rsc = MySQL.search("SELECT * FROM `student_has_class` INNER JOIN `student` ON `student`.`sin`=`student_has_class`.`student_sin` WHERE `class_id`='" + class_id + "'");

            DefaultTableModel dtm = (DefaultTableModel) jTable1.getModel();
            dtm.setRowCount(0);

            while (rsc.next()) {
                Vector v = new Vector();
                v.add(rsc.getString("student_has_class.student_sin"));
                v.add(rsc.getString("student.name"));
                v.add("");

                dtm.addRow(v);
            }

        } catch (Exception e) {
            e.printStackTrace();
        }

    }


