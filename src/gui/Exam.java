    private void jComboBox3ItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_jComboBox3ItemStateChanged
        String examdata = jComboBox3.getSelectedItem().toString();

        if (!examdata.equals("Select")) {
            jTable1.setDefaultEditor(Object.class, null);

            String splitData[] = examdata.split("/", 2);
            String ex_data = splitData[0];
            String c_data = splitData[1];

            String splitData2[] = ex_data.split("-", 3);
            String exId = splitData2[0];
            String exName = splitData2[1];
            String exDate = splitData2[2];

            String splitData3[] = c_data.split("-", 4);
            String cId = splitData3[0];
            String cName = splitData3[1];
            String cYear = splitData3[2];

            jLabel13.setText(exName);
            jLabel7.setText(cId);
            jLabel9.setText(cName + "-" + cYear);
            jLabel14.setText(exDate);
            jLabel7.setForeground(Color.BLUE);
            jLabel9.setForeground(Color.BLUE);
            jLabel13.setForeground(Color.BLUE);
            jLabel14.setForeground(Color.BLUE);

            try {
                ResultSet rse = MySQL.search("SELECT * FROM `exam`\n"
                        + "INNER JOIN `exam_marks` ON `exam`.`id`=`exam_marks`.`exam_id`\n"
                        + "INNER JOIN `student` ON `exam_marks`.`student_sin`=`student`.`sin`\n"
                        + "WHERE `exam`.`id`='" + exId + "' ");

                DefaultTableModel dtm = (DefaultTableModel) jTable1.getModel();
                dtm.setRowCount(0);

                while (rse.next()) {
                    Vector v = new Vector();
                    v.add(rse.getString("exam_marks.student_sin"));
                    v.add(rse.getString("student.name"));
                    v.add(rse.getString("exam_marks.mark"));

                    dtm.addRow(v);
                }

            } catch (Exception e) {
                e.printStackTrace();
            }

            loadMarksSummery();
        }
    }//GEN-LAST:event_jComboBox3ItemStateChanged

    public void printExReport() {
        try {
            SimpleDateFormat sdf2 = new SimpleDateFormat("yyyy-MM-dd HH:mm:ss");
            String dNow = sdf2.format(new Date());

            String c_id = jLabel7.getText();
            String c_name = jLabel9.getText();
            String e_date = jLabel14.getText();
            String e_name = jLabel13.getText();
            String avg = jLabel11.getText();
            String issued = String.valueOf(Login.userId);

            InputStream filePath = getClass().getResourceAsStream("/reports/ex_report.jrxml");
            JasperReport jr = JasperCompileManager.compileReport(filePath);

            HashMap parameters = new HashMap();
            parameters.put("nowdate", dNow);
            parameters.put("issued", issued);
            parameters.put("examname", e_name);
            parameters.put("classid", c_id);
            parameters.put("classname", c_name);
            parameters.put("examdate", e_date);
            parameters.put("avg", avg);

            TableModel tm = jTable1.getModel();
            JRTableModelDataSource dataSource = new JRTableModelDataSource(tm);

            JasperPrint jp = JasperFillManager.fillReport(jr, parameters, dataSource);
            JasperViewer.viewReport(jp, false);
        } catch (Exception e) {
            e.printStackTrace();
        }
    }


