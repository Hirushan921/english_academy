    public void loadMarksSummery() {
        int rc = jTable1.getRowCount();

        double Tmarks = 0;
        double avg = 0;
        for (int i = 0; i < rc; i++) {
            Object m = jTable1.getValueAt(i, 2);
            if (!m.equals("")) {
                String smarks = jTable1.getValueAt(i, 2).toString();
                Tmarks = Tmarks + Double.parseDouble(smarks);
                avg = Tmarks / rc;
            }
        }
        jLabel11.setText(df.format(avg));

    }

