    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed

        String paymentType = jComboBox1.getSelectedItem().toString();
        String payment = jTextField3.getText();

        if (jTable1.getRowCount() == 0) {
            JOptionPane.showMessageDialog(this, "Please add details", "Warning", JOptionPane.WARNING_MESSAGE);
        } else if (!Pattern.compile("(0)|([1-9][0-9]*)|(([1-9][0-9]*)[.]([0]*[1-9][0-9]*))|([0][.]([0]*[1-9][0-9]*))").matcher(payment).matches()) {
            JOptionPane.showMessageDialog(this, "Invalid Payment", "Warning", JOptionPane.WARNING_MESSAGE);
        } else {

            //        invoice_insert
            long mTime = System.currentTimeMillis();
            String uniqueId = mTime + "-" + Login.userId;
            String sin = jLabel6.getText();
            SimpleDateFormat sdf2 = new SimpleDateFormat("yyyy-MM-dd HH:mm:ss");
            String dNow = sdf2.format(new Date());

            MySQL.iud("INSERT INTO `invoice`(`student_sin`,`date_time`,`user_id`,`unique_id`) VALUES('" + sin + "','" + dNow + "','" + Login.userId + "','" + uniqueId + "')");
            //        invoice_insert

            try {
                //        invoice_payment_insert
                ResultSet rs = MySQL.search("SELECT * FROM `invoice` WHERE `unique_id`='" + uniqueId + "'");
                rs.next();
                String id = rs.getString("id");

                ResultSet rs2 = MySQL.search("SELECT * FROM `payment_type` WHERE `name`='" + paymentType + "'");
                rs2.next();
                String paymentTypeId = rs2.getString("id");
                String paymentTypename = rs2.getString("name");

                String balance = jLabel30.getText();

                MySQL.iud("INSERT INTO `invoice_payment`(`invoice_id`,`payment_type_id`,`payment`,`balance`) VALUES('" + id + "','" + paymentTypeId + "','" + payment + "','" + balance + "')");
                //        invoice_payment_insert

                //invoice_item_insert
                for (int i = 0; i < jTable1.getRowCount(); i++) {
                    String classno = jTable1.getValueAt(i, 0).toString();
                    String month = jTable1.getValueAt(i, 2).toString();
                    ResultSet rsm = MySQL.search("SELECT * FROM `month` WHERE `name`='" + month + "'");
                    rsm.next();
                    String month_id = rsm.getString("id");
                    String year = jTable1.getValueAt(i, 3).toString();

                    MySQL.iud("INSERT INTO `invoice_item`(`invoice_id`,`class_id`,`month_id`,`year`) VALUES('" + id + "','" + classno + "','" + month_id + "','" + year + "')");

                }
                //invoice_item_insert

                //invoice report
                try {
                    double pay = Double.parseDouble(payment);
                    String npayment = String.valueOf(pay);
                    String sname = jLabel7.getText();
                    String total = jLabel27.getText();

                    InputStream filePath = getClass().getResourceAsStream("/reports/invomain.jrxml");
                    JasperReport jr = JasperCompileManager.compileReport(filePath);

                    HashMap parameters = new HashMap();
                    parameters.put("date", dNow);
                    parameters.put("invoiceid", uniqueId + "/" + id);
                    parameters.put("sin", sin);
                    parameters.put("name", sname);
                    parameters.put("total", total);
                    parameters.put("payment", npayment + "0");
                    parameters.put("balance", balance);
                    parameters.put("paymethod", "(" + paymentTypename + ")");

                    TableModel tm = jTable1.getModel();
                    JRTableModelDataSource dataSource = new JRTableModelDataSource(tm);

                    JasperPrint jp = JasperFillManager.fillReport(jr, parameters, dataSource);
                    JasperViewer.viewReport(jp, false);
                } catch (Exception e) {
                    e.printStackTrace();
                }
                //invoice report
                resetFields();
                //student
                jButton1.setEnabled(true);
                jTextField1.setEnabled(true);
                jLabel6.setText("None");
                jLabel7.setText("None");
                jLabel8.setText("None");
                jLabel14.setText("None");
                //student

                //payments
                jLabel27.setText("0.00");
                jTextField3.setText("");
                jLabel30.setText("0.00");
                jComboBox1.setSelectedIndex(0);
                jLabel27.setForeground(Color.BLACK);
                jLabel30.setForeground(Color.BLACK);
                //payments

                DefaultTableModel dtm = (DefaultTableModel) jTable1.getModel();
                dtm.setRowCount(0);

                JOptionPane.showMessageDialog(this, "New Invoice Created", "Success", JOptionPane.INFORMATION_MESSAGE);

            } catch (Exception e) {
                e.printStackTrace();
            }
        }
    }//GEN-LAST:event_jButton4ActionPerformed

