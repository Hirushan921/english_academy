    private void jTextField1KeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jTextField1KeyReleased
        if (evt.getKeyCode() == KeyEvent.VK_ENTER) {
            String sin = jTextField1.getText();
            if (sin.isEmpty()) {
                JOptionPane.showMessageDialog(this, "Please select student", "Warning", JOptionPane.WARNING_MESSAGE);
                jTextField1.grabFocus();
            } else {
                try {
                    ResultSet rs = MySQL.search("SELECT * FROM `student` WHERE `sin`='" + sin + "'");
                    if (rs.next()) {
                        if (rs.getString("status_id").equals("2")) {
                            JOptionPane.showMessageDialog(this, "This student deactivated.", "Warning", JOptionPane.WARNING_MESSAGE);
                        } else {
                            jLabel6.setText(sin);
                            jLabel7.setText(rs.getString("name"));
                            jLabel8.setText(rs.getString("contact"));
                            jLabel14.setText(rs.getString("email"));
                            jTextField1.setEnabled(false);
                            jButton1.setEnabled(false);
                        }
                    } else {
                        JOptionPane.showMessageDialog(this, "Invalid SIN", "Warning", JOptionPane.WARNING_MESSAGE);
                        jTextField1.grabFocus();
                    }
                    jTextField1.setText("");

                } catch (Exception e) {
                    e.printStackTrace();
                }
            }

        }
    }//GEN-LAST:event_jTextField1KeyReleased

