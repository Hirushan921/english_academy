    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        String sin = jLabel6.getText();
        String classno = jLabel16.getText();
        String month = jComboBox2.getSelectedItem().toString();
        String year = String.valueOf(jYearChooser1.getYear());

        if (sin.equals("None")) {
            JOptionPane.showMessageDialog(this, "Please select student", "Warning", JOptionPane.WARNING_MESSAGE);
        } else if (classno.equals("None")) {
            JOptionPane.showMessageDialog(this, "Please select class", "Warning", JOptionPane.WARNING_MESSAGE);
        } else if (month.equals("Select")) {
            JOptionPane.showMessageDialog(this, "Please select month", "Warning", JOptionPane.WARNING_MESSAGE);
        } else if (!Pattern.compile("^[2][0-9]{3}$").matcher(year).matches()) {
            JOptionPane.showMessageDialog(this, "Invalid year", "Warning", JOptionPane.WARNING_MESSAGE);
        } else {

            DefaultTableModel dtm = (DefaultTableModel) jTable1.getModel();

            boolean isFound = false;
//            int x = -1;

            for (int i = 0; i < dtm.getRowCount(); i++) {
                String no = jTable1.getValueAt(i, 0).toString();
                String mon = jTable1.getValueAt(i, 2).toString();
                String yr = jTable1.getValueAt(i, 3).toString();

                if ((no.equals(classno)) && (mon.equals(month)) && (yr.equals(year))) {
                    isFound = true;
//                    x = i;
                    break;
                }
            }

            if (isFound) {
                JOptionPane.showMessageDialog(this, "This class payment added", "Warning", JOptionPane.WARNING_MESSAGE);

            } else {

                Vector v = new Vector();
                v.add(classno);
                v.add(jLabel17.getText());
                v.add(month);
                v.add(year);
                v.add(jLabel22.getText());

                dtm.addRow(v);

                updateTotal();

                resetFields();

                JOptionPane.showMessageDialog(this, "This payment added to invoice", "Success", JOptionPane.INFORMATION_MESSAGE);

            }

        }
    }//GEN-LAST:event_jButton3ActionPerformed

